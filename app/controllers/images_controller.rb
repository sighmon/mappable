class ImagesController < ApplicationController
  # GET /images
  # GET /images.json
  def index
    # @images = Image.all
    # @images = Image.order("id").page(params[:page])
    @images = Image.page(params[:page])
    respond_to do |format|
      format.html # index.html.erb
      format.json { render json: @images }
    end
  end

  # GET /images/1
  # GET /images/1.json
  def show
    @image = Image.find(params[:id])

    respond_to do |format|
      format.html # show.html.erb
      format.json { render json: @image }
    end
  end

  # GET /images/new
  # GET /images/new.json
  def new
    @image = Image.new

    respond_to do |format|
      format.html # new.html.erb
      format.json { render json: @image }
    end
  end

  # GET /images/1/edit
  def edit
    @image = Image.find(params[:id])
  end

  # POST /images
  # POST /images.json
  def create
    @image = Image.new(params[:image])

    respond_to do |format|
      if @image.save
        format.html { redirect_to @image, notice: 'Image was successfully created.' }
        format.json { render json: @image, status: :created, location: @image }
      else
        format.html { render action: "new" }
        format.json { render json: @image.errors, status: :unprocessable_entity }
      end
    end
  end

  # PUT /images/1
  # PUT /images/1.json
  def update
    @image = Image.find(params[:id])

    respond_to do |format|
      if @image.update_attributes(params[:image])
        format.html { redirect_to @image, notice: 'Image was successfully updated.' }
        format.json { head :no_content }
      else
        format.html { render action: "edit" }
        format.json { render json: @image.errors, status: :unprocessable_entity }
      end
    end
  end

  # DELETE /images/1
  # DELETE /images/1.json
  def destroy
    @image = Image.find(params[:id])
    @image.destroy

    respond_to do |format|
      format.html { redirect_to images_url }
      format.json { head :no_content }
    end
  end

  def search
    image_results = Image.find_in_range(params[:minimum_latitude], params[:minimum_longitude], params[:maximum_latitude], params[:maximum_longitude])

    respond_to do |format|
      format.json { render :json => { 
        "type" => "FeatureCollection", 
        "features" => image_results.collect{ |i| 
          i.to_geojson_hash(render_to_string(formats: [:html], partial: "popup", locals: {fullsize_url: i.fullsize_url, caption: i.caption}, layout: false))}}.to_json
      }
    end
  end
end
